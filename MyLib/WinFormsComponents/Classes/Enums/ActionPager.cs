

namespace WinFormsComponents.Classes.Enums
{
    /// <summary>
    /// Действия со страничником
    /// </summary>
    /// <remarks>
    /// <para>Next - Следующая страница</para>
    /// <para>Back - Предыдущая страница</para>
    /// <para>Start - Первая страница</para>
    /// <para>End - Последняя страница</para>
    /// <para>Enter - Введенное значение</para>
    /// </remarks>
    public enum ActionPager
    {
        /// <summary>
        /// Следующая страница
        /// </summary>
        /// <value>0</value>
        Next = 0,
        /// <summary>
        /// Предыдущая страница
        /// </summary>
        /// <value>1</value>
        Back = 1,
        /// <summary>
        /// Первая страница
        /// </summary>
        /// <value>2</value>
        Start = 2,
        /// <summary>
        /// Последняя страница
        /// </summary>
        /// <value>3</value>
        End = 3,
        /// <summary>
        /// Введенное значение
        /// </summary>
        /// <value>4</value>
        Enter = 4
    }
}
